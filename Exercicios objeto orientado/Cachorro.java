public class Cachorro{
        private String nome, raça;
        private int idade;
        private double peso;
        private char sexo;
        
        public Cachorro(String novoNome,String novaRaca, int novaIdade,
                        double novoPeso, char novoSexo){
             nome = novoNome;
             raça = novaRaca;
             idade = novaIdade;
             peso = novoPeso;
             sexo = novoSexo;
        }
}