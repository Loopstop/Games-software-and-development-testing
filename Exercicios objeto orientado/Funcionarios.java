public class Funcionarios{
        private String nome;
        private int idade;
        private double altura;
        private char sexo;
        private String sobrenome;
        
        public Funcionarios(String nome, int idade, double altura, char sexo, String sobrenome){
            this.nome = nome;
            this.idade = idade;
            this.altura = altura;
            this.sexo = sexo;
            this.sobrenome=sobrenome;
        }
}